"""# PyTorch callables and custom activations"""

import torch
import torch.nn as nn

# nn.ReLU is a callable object (it has __call__)
relu = nn.ReLU()
input_tensor = torch.tensor([-1.0, 0.0, 2.0])
output_tensor = relu(input_tensor)  # Calls __call__ internally, applies ReLU
print(output_tensor)  # tensor([0., 0., 2.])

# Custom activation as a function
def custom_activation(x):
    return torch.maximum(x, torch.tensor(0.0))

out = custom_activation(torch.tensor([-2.0, 3.0]))
print(out)  # tensor([0., 3.])

# Custom activation as a callable class
class CustomReLU:
    def __call__(self, x):
        return torch.maximum(x, torch.tensor(0.0))

custom_relu = CustomReLU()
result = custom_relu(torch.tensor([-3.0, 4.0]))
print(result)  # tensor([0., 4.])

