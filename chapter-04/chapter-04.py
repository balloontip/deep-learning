"""# Encapsulation via "private" attribute (name-mangled)"""

class RobotSecure:
    def __init__(self, name, function, battery_level):
        self.name = name
        self.function = function
        self.__battery_level = battery_level  # private-like

    def recharge(self, amount):
        self.__battery_level = min(100, self.__battery_level + amount)
        return f"{self.name} recharged to {self.__battery_level}% battery."

    def work(self, hours):
        energy_spent = hours * 10
        if energy_spent > self.__battery_level:
            return f"{self.name} does not have enough battery to work for {hours} hours."
        self.__battery_level -= energy_spent
        return f"{self.name} worked for {hours} hours. Battery now at {self.__battery_level}%"

    def get_battery_status(self):
        return f"{self.name} has {self.__battery_level}% battery remaining."

sweeper = RobotSecure("SweeperBot", "cleaning", 50)
print(sweeper.get_battery_status())
print(sweeper.work(3))
print(sweeper.recharge(40))
print(sweeper.get_battery_status())

