"""# Constants and naming conventions (snake_case, PascalCase)"""

PI = 3.14159  # constant (ALL_CAPS by convention)

def calculate_area(radius):  # snake_case for functions
    return PI * radius * radius

class Circle:  # PascalCase for classes
    def __init__(self, radius):
        self.radius = radius

    def area(self):
        return calculate_area(self.radius)

c = Circle(5)
print(c.area())

